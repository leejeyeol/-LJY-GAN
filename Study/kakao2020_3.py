import numpy as np

def solution(key, lock):
    answer = False
    original_key = np.asarray(key)
    lock = np.asarray(lock)
    M = len(original_key)
    N = len(lock)
    map_size = N + 2*(M-1)
    map_ = np.zeros([map_size, map_size], dtype=int)
    map_[M-1 : M-1+N, M-1 : M-1+N] = lock
    checker = np.ones([N, N], dtype=int)

    for rotation in range(4):
        key = np.rot90(original_key, rotation)
        for i in range(N + M - 1):
            for j in range(N + M - 1):
                submap = map_.copy()
                submap[i : i + M, j: j + M] += key
                if np.array_equal(submap[M - 1: M - 1 + N, M - 1: M - 1 + N], checker):
                    answer = True
                    return answer

    return answer

